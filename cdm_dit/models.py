from diffusers.models import PriorTransformer

class EmbeddingTransformer(nn.Module):
    def __init__(self):
        super().__init__()
        
        self.model = PriorTransformer(
            num_attention_heads=16,
            attention_head_dim=64,
            num_layers=16,
            embedding_dim=512,
            num_embeddings=65,
            additional_embeddings=0,
            encoder_hid_proj_type=None,
            added_emb_type=None,
        )
        self.cond_proj = nn.Embedding(8, 512)

    def forward(self, x, t, y):
        c = self.cond_proj(y)

        y = self.model(hidden_states=x.permute([0,2,1]),
                       timestep=t,
                       proj_embedding=c).predicted_image_embedding
        y = y.permute([0,2,1])

        return y


